 // get a list of users from mysql database
   public ArrayList<Useer> getUsersList()
   {
       ArrayList<Useer> usersList = new ArrayList<Useer>();
       Connection connection = getConnection();
       
       String query = "SELECT * FROM  `studentbase`";
       Statement st;
       ResultSet rs;
       
       try {
           st = connection.createStatement();
           rs = st.executeQuery(query);
           Useer user;
           while(rs.next())
           {
               user = new Useer(rs.getString("UnweightedGPA"),rs.getString("WeightedGPA"),rs.getString("SATScore"),rs.getString("StudentID"), rs.getString("TOEFLScore"), 
                                rs.getString("IBScore"));
               usersList.add(user);
           }
       } catch (Exception e) {
           e.printStackTrace();
       }
       return usersList;
   }
   
   // Display Data In JTable
   
   public void Show_Users_In_JTable()
   {
       ArrayList<Useer> list = getUsersList();
       
       Object[] row = new Object[6];
       for(int i = 0; i < list.size(); i++)
       {
           row[0] = list.get(i).getStudentID();
           row[1] = list.get(i).getUnweightedGPA();
           row[2] = list.get(i).getWeightedGPA();
           row[3] = list.get(i).getSATScore();
           row[4] = list.get(i).getTOEFLScore();
           row[5] = list.get(i).getIBScore();
           
           DefaultTableModel model = (DefaultTableModel) StudentTable.getModel();
           model.addRow(row);
       }
    }
        
   // Execute The Insert Update And Delete Querys
   public void executeSQlQuery(String query, String message)
   {
       Connection con = getConnection();
       Statement st;
       try{
           st = con.createStatement();
           if((st.executeUpdate(query)) == 1)
           {
               // refresh jtable data
               DefaultTableModel model = (DefaultTableModel) StudentTable.getModel();
               model.setRowCount(0);
               Show_Users_In_JTable();
               
               JOptionPane.showMessageDialog(null, "Data "+message+" Succefully");
           }else{
               JOptionPane.showMessageDialog(null, "Data Not "+message);
           }
       }catch(Exception ex){
           ex.printStackTrace();
       }
   }
