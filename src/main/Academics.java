    private void Update_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Update_ButtonActionPerformed
try {
        conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/StudentBase","root","");
         String UnweightedGPA=(String) UnweightedGPA_ComboBox.getSelectedItem();
        String WeightedGPA = (String) WeightedGPA_ComboBox.getSelectedItem();
        String IBScore = (String) TotalIBScore_TextField.getText();
        String TOEFLScore = (String) TOEFLScore_TextField.getText();
        String SATScore = (String) SATSuperscore_TextField.getText();
        
        String query = "update studentbase set UnweightedGPA = ?, WeightedGPA = ?, TOEFLScore = ?, SATScore = ?, IBScore = ? where StudentID = ?";
      PreparedStatement preparedStmt = conn.prepareStatement(query);
      preparedStmt.setString(2, WeightedGPA);
      preparedStmt.setString(1, UnweightedGPA);
      preparedStmt.setString(3, TOEFLScore);
      preparedStmt.setString(4, SATScore);
      preparedStmt.setString(5, IBScore);
      preparedStmt.setString(6, StudentID.getText());
      
        preparedStmt.executeUpdate();
        JOptionPane.showMessageDialog(null, "Data Changed");
        
        
    } catch (SQLException ex) {
        Logger.getLogger(GPA_Yes.class.getName()).log(Level.SEVERE, null, ex);
       
    }
DefaultTableModel model = (DefaultTableModel) StudentTable.getModel();
        model.fireTableDataChanged();
        model.setRowCount(0);
        Show_Users_In_JTable();
// TODO add your handling code here:
    }//GEN-LAST:event_Update_ButtonActionPerformed

